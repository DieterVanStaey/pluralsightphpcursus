<?php

// equal
var_dump(8 == 8);
var_dump(8 == "8");

// identical
var_dump(8 === 8);
var_dump(8 === "8");

// not equal
var_dump(8 <> 9);
var_dump(8 <> "8");
var_dump(8 != 9);
var_dump(8 != "8");

// not identical
var_dump( 8!== 8);
var_dump( 8 !== "8");

// greater than
var_dump(8 > 7);

// greater than or equal to
var_dump(8 >= 8);

// less than
var_dump(8 < 7);

// less than or equal to
var_dump(7 <= 7);